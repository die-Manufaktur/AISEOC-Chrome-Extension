#!/bin/bash
# Test Results Archiver and Trend Analyzer
# Archives results and generates trend analysis
# Exit 0 (always - informational)

# Create reports directory
REPORTS_DIR=".claude/reports/test-results-analyzer"
mkdir -p "$REPORTS_DIR"

# Get current run directory if it exists
if [ -f "$REPORTS_DIR/.current_run" ]; then
    RUN_DIR=$(cat "$REPORTS_DIR/.current_run")
else
    # Create new run directory
    TIMESTAMP=$(date +%Y%m%d_%H%M%S)
    RUN_DIR="$REPORTS_DIR/run_$TIMESTAMP"
    mkdir -p "$RUN_DIR"
fi

# Look for test result files
RESULT_FILES=()
while IFS= read -r file; do
    RESULT_FILES+=("$file")
done < <(find . -maxdepth 3 -type f \( -name "*test-result*.json" -o -name "*test-report*.xml" -o -name "junit*.xml" \) 2>/dev/null)

if [ ${#RESULT_FILES[@]} -gt 0 ]; then
    # Copy results to run directory
    for file in "${RESULT_FILES[@]}"; do
        cp "$file" "$RUN_DIR/" 2>/dev/null
    done

    echo "✅ Archived ${#RESULT_FILES[@]} test result file(s) to: $RUN_DIR" >&2
fi

# Generate trend analysis
TREND_FILE="$REPORTS_DIR/trends.md"

# Count total runs
TOTAL_RUNS=$(find "$REPORTS_DIR" -maxdepth 1 -type d -name "run_*" | wc -l)

# Create or update trends file
cat > "$TREND_FILE" <<EOF
# Test Results Trends

Generated: $(date)

## Summary

- Total analysis runs: $TOTAL_RUNS
- Latest run: $(basename "$RUN_DIR")

## Recent Runs

EOF

# List recent runs (last 10)
find "$REPORTS_DIR" -maxdepth 1 -type d -name "run_*" | sort -r | head -n 10 | while read -r dir; do
    RUN_NAME=$(basename "$dir")
    FILE_COUNT=$(find "$dir" -type f | wc -l)
    echo "- $RUN_NAME ($FILE_COUNT files)" >> "$TREND_FILE"
done

echo "" >> "$TREND_FILE"
echo "---" >> "$TREND_FILE"
echo "*Generated by test-results-analyzer agent*" >> "$TREND_FILE"

echo "📊 Trend analysis updated: $TREND_FILE" >&2

# Cleanup
rm -f "$REPORTS_DIR/.current_run"

exit 0
