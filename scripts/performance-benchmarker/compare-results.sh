#!/bin/bash
# Benchmark Results Comparator
# Generates performance comparison report when agent completes
# Exit 0 (always - informational)

# Create reports directory
REPORTS_DIR=".claude/reports/performance-benchmarker"
mkdir -p "$REPORTS_DIR"

# Check if any benchmark reports were saved
if [ ! -d "$REPORTS_DIR" ] || [ -z "$(ls -A "$REPORTS_DIR" 2>/dev/null)" ]; then
    echo "ℹ️  No benchmark reports to compare" >&2
    exit 0
fi

# Count benchmark runs
BENCHMARK_RUNS=$(find "$REPORTS_DIR" -maxdepth 1 -type d -name "benchmark_*" | wc -l)

if [ "$BENCHMARK_RUNS" -eq 0 ]; then
    echo "ℹ️  No benchmark runs found" >&2
    exit 0
fi

# Create comparison report
COMPARISON_FILE="$REPORTS_DIR/comparison.md"
cat > "$COMPARISON_FILE" <<EOF
# Performance Benchmark Comparison

Generated: $(date)

## Benchmark Runs

EOF

# List all benchmark runs
RUNS=()
for benchmark_dir in "$REPORTS_DIR"/benchmark_*; do
    if [ -d "$benchmark_dir" ]; then
        RUNS+=("$benchmark_dir")
    fi
done

# Sort runs by timestamp (newest first)
IFS=$'\n' SORTED_RUNS=($(sort -r <<<"${RUNS[*]}"))
unset IFS

# Display each run
for benchmark_dir in "${SORTED_RUNS[@]}"; do
    DIR_NAME=$(basename "$benchmark_dir")
    TIMESTAMP=${DIR_NAME#benchmark_}

    # Format timestamp for display
    YEAR=${TIMESTAMP:0:4}
    MONTH=${TIMESTAMP:4:2}
    DAY=${TIMESTAMP:6:2}
    HOUR=${TIMESTAMP:9:2}
    MINUTE=${TIMESTAMP:11:2}
    SECOND=${TIMESTAMP:13:2}

    FORMATTED_DATE="$YEAR-$MONTH-$DAY $HOUR:$MINUTE:$SECOND"

    echo "### Run: $FORMATTED_DATE" >> "$COMPARISON_FILE"
    echo "" >> "$COMPARISON_FILE"

    # Add summary if it exists
    if [ -f "$benchmark_dir/summary.txt" ]; then
        echo "\`\`\`" >> "$COMPARISON_FILE"
        cat "$benchmark_dir/summary.txt" >> "$COMPARISON_FILE"
        echo "\`\`\`" >> "$COMPARISON_FILE"
    fi

    echo "" >> "$COMPARISON_FILE"
    echo "**Files:** [View results](./$DIR_NAME/)" >> "$COMPARISON_FILE"
    echo "" >> "$COMPARISON_FILE"
    echo "---" >> "$COMPARISON_FILE"
    echo "" >> "$COMPARISON_FILE"
done

# Add summary statistics
echo "## Summary" >> "$COMPARISON_FILE"
echo "" >> "$COMPARISON_FILE"
echo "- Total benchmark runs: $BENCHMARK_RUNS" >> "$COMPARISON_FILE"
echo "- Latest run: $(basename "${SORTED_RUNS[0]}")" >> "$COMPARISON_FILE"

# Try to compare latest two runs if available
if [ ${#SORTED_RUNS[@]} -ge 2 ]; then
    LATEST="${SORTED_RUNS[0]}"
    PREVIOUS="${SORTED_RUNS[1]}"

    echo "" >> "$COMPARISON_FILE"
    echo "## Latest vs Previous" >> "$COMPARISON_FILE"
    echo "" >> "$COMPARISON_FILE"
    echo "Comparing:" >> "$COMPARISON_FILE"
    echo "- Latest: $(basename "$LATEST")" >> "$COMPARISON_FILE"
    echo "- Previous: $(basename "$PREVIOUS")" >> "$COMPARISON_FILE"
    echo "" >> "$COMPARISON_FILE"

    # Try to extract metrics for comparison
    if [ -f "$LATEST/lighthouse-report.json" ] && [ -f "$PREVIOUS/lighthouse-report.json" ]; then
        echo "### Lighthouse Score Changes" >> "$COMPARISON_FILE"
        echo "" >> "$COMPARISON_FILE"

        # Compare performance scores
        LATEST_PERF=$(jq -r '.categories.performance.score // 0' "$LATEST/lighthouse-report.json" 2>/dev/null || echo "0")
        PREVIOUS_PERF=$(jq -r '.categories.performance.score // 0' "$PREVIOUS/lighthouse-report.json" 2>/dev/null || echo "0")

        if [ "$LATEST_PERF" != "0" ] && [ "$PREVIOUS_PERF" != "0" ]; then
            DIFF=$(echo "scale=2; ($LATEST_PERF - $PREVIOUS_PERF) * 100" | bc 2>/dev/null || echo "0")
            echo "- Performance: $LATEST_PERF (${DIFF:+$DIFF})" >> "$COMPARISON_FILE"
        fi
    fi
fi

echo "" >> "$COMPARISON_FILE"
echo "---" >> "$COMPARISON_FILE"
echo "*Generated by performance-benchmarker agent*" >> "$COMPARISON_FILE"

echo "✅ Performance comparison report generated: $COMPARISON_FILE" >&2
echo "   Total benchmark runs: $BENCHMARK_RUNS" >&2

exit 0
